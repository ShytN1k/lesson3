<?php

namespace Animals;

abstract class Bird implements AnimalsInterface
{
    use GeneralFunctions;

    private $owner;

    /**
     * @return mixed
     */
    public function getOwner()
    {
        return $this->owner;
    }

    /**
     * @param mixed $owner
     */
    public function setOwner($owner)
    {
        $this->owner = $owner;
    }
}